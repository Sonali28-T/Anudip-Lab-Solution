#3. Using max() and min() functions display the maximum and minimum of 5 random numbers.
#Solution=>>

# Define a list of 5 specific numbers
numbers = [72, 99, 7, 89, 36]

# Find the maximum number using the max() function
max_number = max(numbers)

# Find the minimum number using the min() function
min_number = min(numbers)

# Display the list of numbers
print(f"The numbers are: {numbers}")

# Display the maximum number
print(f"The maximum number is: {max_number}")

# Display the minimum number
print(f"The minimum number is: {min_number}")

"""ANS=>> The numbers are: [72, 99, 7, 89, 36]
          The maximum number is: 99
          The minimum number is: 7
"""
