# 2. Declare two variables and print that which variable is largest using ternary operators
#Define two Variables representing student scores:
score_student1=70
score_student2=99
# Use a ternary operator to determine which student's score is higher
# If score_student1 is greater than score_student2, assign score_student1 to highest_score, Otherwise assign score_student2 to highest_score
highest_score = score_student1 if score_student1 > score_student2 else score_student2
# Print the highest score
print("The highest score is:", highest_score)

#ANS=>> The highest score is:99


