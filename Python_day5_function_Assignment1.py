#1. Declare a div() function with two parameters. Then call the function and pass two numbers and display their division.
#Solution=>>

# Define a function named 'div' that takes two parameters 'a' and 'b'
def div(a, b):
    # Return the result of dividing 'a' by 'b'
    return a / b

# Call the 'div' function with two numbers as arguments
result = div(14, 2)

# Display the result of the division
print("The division of 14 by 2 is:", result)

#ANS=>> The division of 14 by 2 is: 7.0

